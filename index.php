	<p>Laurin Penland is a writer and <a id="regLink" href="https://library.duke.edu/about/directory/staff/laurin.penland">memory worker</a> living in Durham, North Carolina. Penland's fiction, creative nonfiction, and journalism can be found at <a href="https://www.saltfront.org/"><i>saltfront: studies in human habit(at)</i></a>, <i>Lute & Drum</i>, <a href="http://palaverjournal.com/"><i>Palaver</i></a>, and <a href="https://www.npr.org/"><i>NPR</i></a>. Penland grew up in small-town Appalachia and received an MFA from the University of North Carolina at Wilmington.</p>
	<p>Penland's writing is often concerned with Appalachia, queer survival, music, legacies of violence, and the sacredness of beings.</p>
